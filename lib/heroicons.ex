defmodule Heroicons do
  @moduledoc """
  [Heroicons](https://heroicons.com/) components for Phoenix LiveView.

  ## Usage

  Default class ("flex-shrink-0 h-5 w-5"):

  ```
  <Heroicons.Solid.home />
  <Heroicons.Outline.home />
  ```

  Use a custom class:

  ```
  <Heroicons.Solid.home class="h-8 w-8" />
  ```

  Any extra attributes are added to the `svg` element:

  ```
  <Heroicons.Solid.home x-show="open" class="block h-6 w-6" />
  ```

  """
end
